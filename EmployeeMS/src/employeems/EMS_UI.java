    private void hireBtn_HActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hireBtn_HActionPerformed
        String eName = eNameTxt_H.getText();
        Date eDOA = (Date) eDOATxt_H.getValue();
        int eAge, eSalary;
        try{
            eAge = Integer.parseInt(eAgeTxt_H.getText());
            eSalary = Integer.parseInt(eSalaryTxt_H.getText());
        }catch(Exception e){
           eAge = eSalary = 0; 
        }
        boolean somethingMissing = false;
        if(eName.equals("")){
            eNameTxt_H.setBackground(Color.red);
            somethingMissing = true;
        } 
        if(eAge <= 0){
            eAgeTxt_H.setBackground(Color.red);
            somethingMissing = true;
        }
        if(eSalary <= 0){
            eSalaryTxt_H.setBackground(Color.red);
            somethingMissing = true;
        }
        if(somethingMissing)
            return;
        else{
            Formatter f = new Formatter();
            f.format("%te-%th-%tY", eDOA, eDOA, eDOA);
            boolean done = employeeManagmentSystem.hireEmployee(eName, eSalary, eAge, f.toString());
            if(done){
                result_H.setText("Succes");
                eNameTxt_H.setText("");
                eAgeTxt_H.setText("");
                eSalaryTxt_H.setText("");
            }
            else
                result_H.setText("Failed");
        }
    }//GEN-LAST:event_hireBtn_HActionPerformed

    private void eIdBtn_FActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eIdBtn_FActionPerformed
        result_F.setText("");
        String eId = eIdTxt_F.getText();
        if(eId.equals("")){
            eIdTxt_F.setBackground(Color.red);
            return;
        }
        else{
            try{
               String[] data = employeeManagmentSystem.fireEmployee( Integer.parseInt(eId) );
               if(data == null){
                   result_F.setText("Invalid Employee ID, Failed!");
               }
               else{
                eAgeTxt_F.setText(data[1]); 
                eNameTxt_F.setText(data[0]);
                eSalaryTxt_F.setText(data[2]);
                eDOATxt_F.setText(data[3]);
                fireBtn_F.setEnabled(true);
               }
            }catch(Exception e){e.printStackTrace();}
        }
    }//GEN-LAST:event_eIdBtn_FActionPerformed

