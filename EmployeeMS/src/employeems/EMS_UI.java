    private void generateRprtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateRprtBtnActionPerformed
        String rType = rprtMenuList.getSelectedValue();
        if(rType.equals("Search Employee With Name"))
            rType += ">" + JOptionPane.showInputDialog(this, "Enter Employee\'s Name to Search?");
        
        try{
            ResultSet r = employeeManagmentSystem.GenerateReport(rType);
            if(r == null )
                return;
            else{
                for(int i = 0; i < rprtResult.getRowCount(); i++)
                        for(int j = 0; j < rprtResult.getColumnCount(); j++)
                            rprtResult.setValueAt("", i, j);
                if(rType.equals("Show Salary Details")){
                    String str = "";
                    if(r.next()){
                        str += "All employee\'s Salary detail:\n";
                        str += "Average Salary = " + r.getObject(1) + "\n";
                        str += "Max Salary = " + r.getObject(2) + "\n";
                        str += "Min Salary = " + r.getObject(3) + "\n";
                        str += "Sum of Salaries = " + r.getObject(4) + "\n";
                        str += "of " + r.getObject(5) + "(All) Employees.\n";
                    }
                    JOptionPane.showMessageDialog(this, str);
                }
                else if(rType.equals("Summary on Employee\'s Record")){
                    String str = "";
                    if(r.next()){
                        str += "Summary on Employee\'s Record :\n";
                        str += "No of Employee = " + r.getObject(1) + "\n";
                        str += "Max Salary of an employee = " + r.getObject(2) + "\n";
                        str += "Min Salary of an employee = " + r.getObject(3) + "\n";
                        str += "Max Age of an employee = " + r.getObject(4) + "\n";
                        str += "Min Age of an employee = " + r.getObject(5) + "\n";
                        str += "Max D.O.A of an employee = " + r.getObject(6) + "\n";
                        str += "Min D.O.A of an employee = " + r.getObject(7) + "\n";
                    }
                    JOptionPane.showMessageDialog(this, str);
                }
                else{
                    int i;
                    for(i = 0; r.next(); i++){
                        for(int j = 0; j < 5; j++)
                            rprtResult.setValueAt(r.getObject(j+1)+"", i, j);
                    }
                    if(i == 0)
                        for(int j = 0; j < 5; j++)
                            rprtResult.setValueAt("No Result!", i, j);
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_generateRprtBtnActionPerformed

